/*: WAP print Square of each number from 1 to 20:
     Number       Square
      4                   16*/

class Loop_Square

 {

   public static void main(String[]args)

          {

 
      int n=1;

System.out.println("\n\t\t\tNumber         Square");



while(n<=20)

{
int Square=n*n;
System.out.println("\n\t\t\t" +n+"                "+Square);

n++;

      }
  }
}


